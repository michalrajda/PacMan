int MapLevel::_actualLevel[_height][_width]{};

int MapLevel::_mapLevelSource[_height][_width]{};

TunelSize MapLevel::_tunelSize{};

TunelSize MapLevel::_tunelSource{};

void  MapMenager::loadMap(int&& level, const char map[MapLevel::_height][MapLevel::_width], PacManMenager& pacman, PointMenager& pointComp, BoostMenager& boost, GhostMenager& ghostMenager) noexcept
{
	int width = MapLevel::_width;
	int height = MapLevel::_height;
	pacman.clear();
	pointComp.clear();
	pointComp.resizeSparse(height - 2, width - 2, -2);
	ghostMenager.clear();
	ghostMenager.resizeAllNodesPath(height - 2, width - 2);
	ghostMenager.resizeAllEnds(4);
	boost.clear();
	clear();
	SDL_Rect sGrassAndWall{};
	for (int y = 0; y < height; ++y)
	{
		for (int x = 0; x < width; ++x)
		{
			MapLevel::_actualLevel[y][x] = map[y][x];
			if ((x == 1 && y > 0 && y < (height - 1)) || (y == 1 && x > 0 && x < (width - 1)))
			{
				if (map[y][x] == 1)
				{
					ghostMenager.pushBackPath(x - 2, y - 2, true);
				}
				if (map[y][x] == 33)
				{
					ghostMenager.pushBackPath(x - 2, y - 2, false);
					MapLevel::_tunelSize._left.x = static_cast<float>(x - 2) * 32.0f;
					MapLevel::_tunelSize._left.y = static_cast<float>(y - 2) * 32.0f;
					MapLevel::_actualLevel[y][x] = 0;
				}
				else if (map[y][x] == 44)
				{
					ghostMenager.pushBackPath(x - 2, y - 2, false);
					MapLevel::_tunelSize._right.x = static_cast<float>(x - 2) * 32.0f;
					MapLevel::_tunelSize._right.y = static_cast<float>(y - 2) * 32.0f;
					MapLevel::_actualLevel[y][x] = 0;
				}
			}
			if ((x == (width - 2) && y > 0 && y < (height - 1)) || (y == (height - 2) && x > 0 && x < (width - 1)))
			{
				if (map[y][x] == 1)
				{
					ghostMenager.pushBackPath(x - 2, y - 2, true);
				}
				if (map[y][x] == 33)
				{
					ghostMenager.pushBackPath(x - 2, y - 2, false);
					MapLevel::_tunelSize._left.x = static_cast<float>(x - 2) * 32.0f;
					MapLevel::_tunelSize._left.y = static_cast<float>(y - 2) * 32.0f;
					MapLevel::_actualLevel[y][x] = 0;
				}
				else if (map[y][x] == 44)
				{
					ghostMenager.pushBackPath(x - 2, y - 2, false);
					MapLevel::_tunelSize._right.x = static_cast<float>(x - 2) * 32.0f;
					MapLevel::_tunelSize._right.y = static_cast<float>(y - 2) * 32.0f;
					MapLevel::_actualLevel[y][x] = 0;
				}
			}
			if ((x > 1 && x < (width - 2)) && (y > 1 && y < (height - 2)))
			{
				sGrassAndWall.x = (x - 2) * 32;
				sGrassAndWall.y = (y - 2) * 32;
				sGrassAndWall.w = 32;
				sGrassAndWall.h = 32;
				if (map[y][x] == 1)
				{
					_mapSize.emplace_back(1, sGrassAndWall);
					ghostMenager.pushBackPath(x - 2, y - 2, true);
				}
				else
				{
					_mapSize.emplace_back(0, sGrassAndWall);
					ghostMenager.pushBackPath(x - 2, y - 2, false);
					if (map[y][x] == 2)
					{
						MapLevel::_actualLevel[y][x] = 0;
						pacman.getPacMan()[0].first = 0;
						pacman.getPacMan()[0].second.x = (x - 2.0f) * 32.0f;
						pacman.getPacMan()[0].second.y = (y - 2.0f) * 32.0f;
						pacman.getPacMan()[0].second.h = pacman.getPacMan()[0].second.w = 32.0f;
						pacman.getPacMan()[1] = pacman.getPacMan()[0];
					}
					else if (map[y][x] == 5)
					{
						boost.emplace_back(x - 2, y - 2, level);
						pointComp.push_backPoints(x - 2, y - 2, width);
						MapLevel::_actualLevel[y][x] = 0;
					}
					else if (map[y][x] == 11)
					{
						_mapSize.emplace_back(2, sGrassAndWall);
						MapLevel::_actualLevel[y][x] = 1;
					}
					else if (map[y][x] == 12)
					{
						_mapSize.emplace_back(3, sGrassAndWall);
						MapLevel::_actualLevel[y][x] = 1;
					}
					else if (map[y][x] == 13)
					{
						_mapSize.emplace_back(4, sGrassAndWall);
						MapLevel::_actualLevel[y][x] = 1;
					}
					else if (map[y][x] == 14)
					{
						_mapSize.emplace_back(5, sGrassAndWall);
						MapLevel::_actualLevel[y][x] = 1;
					}
					else if (map[y][x] == 60)
					{
						ghostMenager.pushBackGhostEnd(x - 2, y - 2, 0, 0);
						boost.emplace_back(x - 2, y - 2, level);
						pointComp.push_backPoints(x - 2, y - 2, width);
						MapLevel::_actualLevel[y][x] = 0;
					}
					else if (map[y][x] == 61)
					{
						ghostMenager.pushBackGhostEnd(x - 2, y - 2, 0, 1);
						boost.emplace_back(x - 2, y - 2, level);
						pointComp.push_backPoints(x - 2, y - 2, width);
						MapLevel::_actualLevel[y][x] = 0;
					}
					else if (map[y][x] == 62)
					{
						ghostMenager.pushBackGhostEnd(x - 2, y - 2, 0, 2);
						boost.emplace_back(x - 2, y - 2, level);
						pointComp.push_backPoints(x - 2, y - 2, width);
						MapLevel::_actualLevel[y][x] = 0;
					}
					else if (map[y][x] == 63)
					{
						ghostMenager.pushBackGhostEnd(x - 2, y - 2, 0, 3);
						MapLevel::_actualLevel[y][x] = 0;
					}
					else if (map[y][x] == 70)
					{
						ghostMenager.pushBackGhostEnd(x - 2, y - 2, 1, 0);
						boost.emplace_back(x - 2, y - 2, level);
						pointComp.push_backPoints(x - 2, y - 2, width);
						MapLevel::_actualLevel[y][x] = 0;
					}
					else if (map[y][x] == 71)
					{
						ghostMenager.pushBackGhostEnd(x - 2, y - 2, 1, 1);
						boost.emplace_back(x - 2, y - 2, level);
						pointComp.push_backPoints(x - 2, y - 2, width);
						MapLevel::_actualLevel[y][x] = 0;
					}
					else if (map[y][x] == 72)
					{
						ghostMenager.pushBackGhostEnd(x - 2, y - 2, 1, 2);
						boost.emplace_back(x - 2, y - 2, level);
						pointComp.push_backPoints(x - 2, y - 2, width);
						MapLevel::_actualLevel[y][x] = 0;
					}
					else if (map[y][x] == 73)
					{
						ghostMenager.pushBackGhostEnd(x - 2, y - 2, 1, 3);
						MapLevel::_actualLevel[y][x] = 0;
					}
					else if (map[y][x] == 80)
					{
						ghostMenager.pushBackGhostEnd(x - 2, y - 2, 2, 0);
						boost.emplace_back(x - 2, y - 2, level);
						pointComp.push_backPoints(x - 2, y - 2, width);
						MapLevel::_actualLevel[y][x] = 0;
					}
					else if (map[y][x] == 81)
					{
						ghostMenager.pushBackGhostEnd(x - 2, y - 2, 2, 1);
						boost.emplace_back(x - 2, y - 2, level);
						pointComp.push_backPoints(x - 2, y - 2, width);
						MapLevel::_actualLevel[y][x] = 0;
					}
					else if (map[y][x] == 82)
					{
						ghostMenager.pushBackGhostEnd(x - 2, y - 2, 2, 2);
						boost.emplace_back(x - 2, y - 2, level);
						pointComp.push_backPoints(x - 2, y - 2, width);
						MapLevel::_actualLevel[y][x] = 0;
					}
					else if (map[y][x] == 83)
					{
						ghostMenager.pushBackGhostEnd(x - 2, y - 2, 2, 3);
						MapLevel::_actualLevel[y][x] = 0;
					}
					else if (map[y][x] == 90)
					{
						ghostMenager.pushBackGhostEnd(x - 2, y - 2, 3, 0);
						boost.emplace_back(x - 2, y - 2, level);
						pointComp.push_backPoints(x - 2, y - 2, width);
						MapLevel::_actualLevel[y][x] = 0;
					}
					else if (map[y][x] == 91)
					{
						ghostMenager.pushBackGhostEnd(x - 2, y - 2, 3, 1);
						boost.emplace_back(x - 2, y - 2, level);
						pointComp.push_backPoints(x - 2, y - 2, width);
						MapLevel::_actualLevel[y][x] = 0;
					}
					else if (map[y][x] == 92)
					{
						ghostMenager.pushBackGhostEnd(x - 2, y - 2, 3, 2);
						boost.emplace_back(x - 2, y - 2, level);
						pointComp.push_backPoints(x - 2, y - 2, width);
						MapLevel::_actualLevel[y][x] = 0;
					}
					else if (map[y][x] == 93)
					{
						ghostMenager.pushBackGhostEnd(x - 2, y - 2, 3, 3);
						MapLevel::_actualLevel[y][x] = 0;
					}
				}
			}
		}
	}
	ghostMenager.set_neighboursAllPath(height - 2, width - 2);
}

